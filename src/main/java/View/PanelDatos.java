    // Método para recibir y mostrar los datos del administrador en los campos del formulario
    public void setAdministrador(Administrador admin) {
        this.administradorActual = admin; // Guarda el objeto completo

        if (admin != null) {
            // Muestra los datos actuales en los campos de texto
            txtNombre.setText(admin.getNombre());
            txtApellido.setText(admin.getApellido());
            txtCorreo.setText(admin.getCorreo());
        } else {
            // Si no se recibe un administrador, limpia los campos
            txtNombre.setText("");
            txtApellido.setText("");
            txtCorreo.setText("");
        }
    }
    
