    // Acción del botón para actualizar los datos del administrador
    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        // Opciones personalizadas para la confirmación
        String[] opciones = {"Sí", "No"};

        // Mostrar cuadro de confirmación al usuario antes de actualizar
        int confirmacion = JOptionPane.showOptionDialog(this,
                "¿Deseas actualizar tus datos?",
                "Confirmar actualización",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE,
                null, opciones, opciones[0]);

        if (confirmacion == JOptionPane.YES_OPTION) {
            // Obtener los nuevos valores ingresados en los campos
            String nuevoNombre = txtNombre.getText().trim();
            String nuevoApellido = txtApellido.getText().trim();
            String nuevoCorreo = txtCorreo.getText().trim();

            // Validación simple para asegurar que ningún campo quede vacío
            if (nuevoNombre.isEmpty() || nuevoApellido.isEmpty() || nuevoCorreo.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Todos los campos deben estar completos.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Cancelar si faltan datos
            }

            // Actualizar los valores en el objeto administradorActual
            administradorActual.setNombre(nuevoNombre);
            administradorActual.setApellido(nuevoApellido);
            administradorActual.setCorreo(nuevoCorreo);

            // Intentar guardar los cambios en la base de datos
            boolean exito = gestor.modificarDatos(administradorActual);

            if (exito) {
                // Mostrar mensaje de éxito 
                JOptionPane.showMessageDialog(this, "Datos actualizados correctamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            } else {
                // Mostrar mensaje de error si la actualización falla
                JOptionPane.showMessageDialog(this, "Hubo un error al actualizar los datos.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

